interface I18nContextValue {
  lang: LanguageCode;
  currency: CurrencyCode;
  t: (key: string) => string;
  setLang: (lang: LanguageCode) => void;
  setCurrency: (currency: CurrencyCode) => void;
  formatCurrency: (amountUsd: number | string) => string;
}

const I18nContext = createContext<I18nContextValue>({
  lang: "en",
  currency: "USD",
  t: (key) => key,
  setLang: () => {},
  setCurrency: () => {},
  formatCurrency: (v) => String(v),
});

export function I18nProvider({
  children,
  initialLang,
  initialCurrency,
}: {
  children: ReactNode;
  initialLang: LanguageCode;
  initialCurrency?: CurrencyCode;
}) {
  const [lang, setLangState] = useState<LanguageCode>(initialLang);
  const [currency, setCurrencyState] = useState<CurrencyCode>(
    initialCurrency ?? LANGUAGE_META[initialLang].currency
  );

  const setLang = useCallback((next: LanguageCode) => {
    setLangState(next);
  }, []);

  const setCurrency = useCallback((next: CurrencyCode) => {
    setCurrencyState(next);
  }, []);

  const t = useCallback(
    (key: string) => {
      return DICTIONARY[lang]?.[key] ?? DICTIONARY.en[key] ?? key;
    },
    [lang]
  );

  const formatCurrency = useCallback(
    (amountUsd: number | string) => fmt(amountUsd, currency),
    [currency]
  );

  return (
    <I18nContext.Provider value={{ lang, currency, t, setLang, setCurrency, formatCurrency }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}
